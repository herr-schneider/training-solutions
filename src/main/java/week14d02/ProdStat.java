package week14d02;

public class ProdStat {
    private String name;
    private int quant;

    public ProdStat(String name, int quant) {
        this.name = name;
        this.quant = quant;
    }

    public String getName() {
        return name;
    }

    public int getQuant() {
        return quant;
    }
}
