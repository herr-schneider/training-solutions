package methodchain.robot;

public class RobotCoordinates {
    final double x;
    final double y;

    public RobotCoordinates(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
